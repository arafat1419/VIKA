package com.vika.sdk.models

/**
 * Display modes for the VIKA SDK UI.
 */
enum class VikaDisplayMode {
    /**
     * Full-screen activity mode (default).
     */
    FULLSCREEN,

    /**
     * Centered dialog with rounded corners and dimmed background.
     */
    DIALOG,

    /**
     * Bottom sheet that slides up from the bottom of the screen.
     */
    BOTTOM_SHEET
}
